#include <unistd.h>

int	ft_str_is_lowercase(char *str)
{
	int	n;

	n = 0;
	while (str[n] != '\0')
	{
		if (str[n] < 'a' || str[n] > 'z')
			return (0);
		n++;
	}
	return (1);
}
